int firstNumber = 3;
int secondNumber = 9;

void example() {
  // if-else methodu
  if (firstNumber > secondNumber) {
    print("Birinci sayı, ikinci sayıdan büyüktür.");
  } else {
    print("Küçüktür.");
  }

  // switch-case methodu
  switch (firstNumber) {
    case 1:
      print("sayı 1");
      break;
    case 2:
      print("sayı 2");
      break;
    case 3:
      print("sayı 3");
      break;
    case 4:
      print("sayı 4");
      break;
  }
}
